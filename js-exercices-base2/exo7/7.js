

function faireChoix() {
    var num = prompt("choisi entre 1,2,3");

    switch (num) {
        case "1":
            alert('merci');
            break;
        case "2":
            alert('bonjours');
            break;
        case "3":
            alert('Au revoir');
            break;
        default:
            alert('blabla');
            break;

    }
}
